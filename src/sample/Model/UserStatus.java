package sample.Model;

import java.util.List;

public class UserStatus extends AbstractModel {

    private int id;
    private String name;
    private List<String> users;

    public UserStatus(int id, String name, List<String> users) {
        this.id = id;
        this.name = name;
        this.users = users;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public List<String> getUsers() {
        return users;
    }

    public void setUsers(List<String> users) {
        this.users = users;
    }
}
